package tuesdayRevision;

public class EscapeCharacters {

    public static void main(String[] args) {

        System.out.println("\" Nobody can help me without my permission\" -Ghandi");//This is how to print a quotation with \

        //      \n = new line
        System.out.println("\" Nobody can help me without my permission\" \n-Ghandi");//This is how to print authors below their qoutes

//      \t = tab
        System.out.println("\" Nobody can help me without my permission\" \t\t-Ghandi");// This will move two tab


//      \b = backspace
        System.out.println("\" Nobody can help me without my permission\" \n-Ghandisss\b\b\b");// using backspace


    }
}
